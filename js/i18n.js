function changeLanguage(lang) {
    if (!translations[lang]) return;

    // Save preference
    localStorage.setItem('medassist_lang', lang);

    // Apply translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key] !== undefined) {
            el.innerHTML = translations[lang][key]; // innerHTML allows formatting like <br> if needed, though innerText is safer normally. We only use text.
        }
    });

    // Update dynamically changing fonts via CSS Variables on :root
    const rootStyles = document.documentElement.style;
    if (lang === 'hi') {
        rootStyles.setProperty('--dynamic-font-sans', "'Noto Sans Devanagari', sans-serif");
        rootStyles.setProperty('--dynamic-font-heading', "'Noto Sans Devanagari', sans-serif");
    } else if (lang === 'te') {
        rootStyles.setProperty('--dynamic-font-sans', "'Noto Sans Telugu', sans-serif");
        rootStyles.setProperty('--dynamic-font-heading', "'Noto Sans Telugu', sans-serif");
    } else {
        // En defaults
        rootStyles.setProperty('--dynamic-font-sans', "'Inter', system-ui, sans-serif");
        rootStyles.setProperty('--dynamic-font-heading', "'Outfit', system-ui, sans-serif");
    }
    
    // Update language select if exists
    const langSelect = document.getElementById('language-select');
    if (langSelect && langSelect.value !== lang) {
        langSelect.value = lang;
    }
}

// Initial hydration on DOMContentLoaded
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('medassist_lang') || 'en';
    
    const langSelect = document.getElementById('language-select');
    if (langSelect) {
        langSelect.value = savedLang;
        langSelect.addEventListener('change', (e) => {
            changeLanguage(e.target.value);
        });
    }

    if (savedLang !== 'en') {
        changeLanguage(savedLang);
    }
    
    // Auto-convert native selects to sleek custom UI
    const langSelects = document.querySelectorAll('.lang-selector');
    langSelects.forEach(select => {
        if(select.nextElementSibling && select.nextElementSibling.classList.contains('custom-lang-selector')) return;
        
        select.style.display = 'none'; // Hide native select
        
        const customWrapper = document.createElement('div');
        customWrapper.className = 'custom-lang-selector';
        
        const trigger = document.createElement('div');
        trigger.className = 'custom-lang-trigger';
        const activeOption = select.options[select.selectedIndex] || select.options[0];
        trigger.innerHTML = `<span class="lang-text">${activeOption.text}</span> <span style="font-size: 0.7em;">▼</span>`;
        
        const optionsContainer = document.createElement('div');
        optionsContainer.className = 'custom-lang-options';
        
        Array.from(select.options).forEach(opt => {
            const optDiv = document.createElement('div');
            optDiv.className = 'custom-lang-option' + (opt.selected ? ' active' : '');
            optDiv.dataset.value = opt.value;
            optDiv.innerText = opt.text;
            
            optDiv.addEventListener('click', () => {
                select.value = opt.value;
                select.dispatchEvent(new Event('change'));
                
                trigger.querySelector('.lang-text').innerText = opt.text;
                optionsContainer.querySelectorAll('.custom-lang-option').forEach(o => o.classList.remove('active'));
                optDiv.classList.add('active');
                optionsContainer.classList.remove('open');
            });
            optionsContainer.appendChild(optDiv);
        });
        
        trigger.addEventListener('click', (e) => {
            e.stopPropagation();
            optionsContainer.classList.toggle('open');
        });
        
        document.addEventListener('click', () => {
            optionsContainer.classList.remove('open');
        });
        
        customWrapper.appendChild(trigger);
        customWrapper.appendChild(optionsContainer);
        select.parentNode.insertBefore(customWrapper, select.nextSibling);

        select.addEventListener('change', () => {
             const newActive = select.options[select.selectedIndex];
             trigger.querySelector('.lang-text').innerText = newActive.text;
             optionsContainer.querySelectorAll('.custom-lang-option').forEach(o => {
                 if (o.dataset.value === newActive.value) o.classList.add('active');
                 else o.classList.remove('active');
             });
        });
    });
});
